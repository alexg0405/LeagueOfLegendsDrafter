export function resolveChampionName(
  championId: number,
  idToName: ReadonlyMap<number, string> | null
): string {
  const live = idToName?.get(championId)
  if (live) {
    return live
  }
  return BUNDLED_CHAMPION_NAMES[championId] ?? `Champion ${championId}`
}
